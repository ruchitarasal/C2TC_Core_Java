package src;

public class exampleofSwitch {

	public static void main(String[] args) {
	int x=0;
	switch(x)
	{default:
		System.out.println("Default");
		break;
		
	case 0:	
		System.out.println(0);
		break;
		
	case 1:	
		System.out.println(1);
		break;
		
	case 2:	
		System.out.println(2);
		break;
	
	
	}
	}
}

